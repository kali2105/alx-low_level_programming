#include "3-calc.h"

/**
 * op_add - A function that calculate the sum of two numbers of integer type
 * @a: The first number of type integer
 * @b: The second number of type integer
 *
 * Return: The Results of the operation given
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - A function that calculate the diff of two numbers of integer type
 * @a: The first number of type integer
 * @b: The second number of type integer
 *
 * Return: The Results of the operation given
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - A function that calculate the multp of two numbers of integer type
 * @a: The first number of type integer
 * @b: The second number of type integer
 *
 * Return: The Results of the operation given
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_mod - A function that calculate the module of two numbers of integer type
 * @a: The first number of type integer
 * @b: The second number of type integer
 *
 * Return: The Results of the operation given
 */

int op_mod(int a, int b)
{
	return (a % b);
}

/**
 * op_div - A function that calculate the div of two numbers of integer type
 * @a: The first number of type integer
 * @b: The second number of type integer
 *
 * Return: The Results of the operation given
 */

int op_div(int a, int b)
{
	return (a / b);
}
